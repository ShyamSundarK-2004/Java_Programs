import java.util.Scanner;

public class Check_Divisibility {
    public static void main(String[] args) {
    	int a = 10;
    	a = a++;  
    	System.out.println(a);  // Print a
    	int b = a;  
    	int c = a++;  
    	System.out.println(a);  // Print a
    	System.out.println(b);  // Print b
    	System.out.println(c);  // Print c
    }
    
}

