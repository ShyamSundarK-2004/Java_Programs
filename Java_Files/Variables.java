class Variables{

public static void main(String [] args){
	int n;
	n = 5; //intializing 
	System.out.println("Storing and retriveing");
	System.out.println(n);	//retriveing 
	System.out.println("Modifiying");
	int a = 10;
	System.out.println(a);
	a = 20;
	System.out.println(a);
	
	System.out.println("updating");
	int b = 15;
	System.out.println(b);
	b = b+5;
	System.out.println(b);

}
}