package java_codes_Objects_Constructors;

public class Movie {

	String name;
	String director;
	String hero;
	String heroine;
	String collection;

	Movie(String name, String director, String hero, String heroine, String collection) {
		this.name = name;
		this.director = director;
		this.hero = hero;
		this.heroine = heroine;
		this.collection = collection;
	}

}
